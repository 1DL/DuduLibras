    private boolean compararNuvemMao(String Nuvem1, String Nuvem2, String MaoSTR, String Simbolo) {
        int nvm1 = Integer.parseInt(Nuvem1.substring(17, 18));
        int nvm2 = Integer.parseInt(Nuvem2.substring(17, 18));

        int mao;

        if ("D".equals(MaoSTR.substring(28, 29))) {
            mao = 10;
        } else {
            mao = Integer.parseInt(MaoSTR.substring(28, 29));
        }

        int res;

        if ("+".equals(Simbolo)) {
            res = nvm1 + nvm2;
            return mao == res;
        } else {
            res = nvm1 - nvm2;
            return mao == res;
        }

    }

